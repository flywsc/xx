package com.wsc201821;

public class _12 {
	public static void main(String[] args) {
      //获取指定位置的字符
      String st="姓王名串";
      String st1="姓王名串";
      char xing=st.charAt(1);//1代表索引位置，所以length要减一个
      char ming=st.charAt(st1.length()-1);
      //字符长度用length()
      //数组长度用lengthz
      System.out.println(xing);
      System.out.println(ming);
      
	
	}
}
